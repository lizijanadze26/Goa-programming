//დაწერეთ კოდი, რომელიც აიღებს მასივს, მაგლითად: [5, 12, 7, 20, 3, 8], თუ რიცხვი მეტია 10ზე, გამოიტანოს "დიდი", სხვა შემთხვევაში "პატარა"
