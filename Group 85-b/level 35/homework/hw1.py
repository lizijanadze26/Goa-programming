#2) დაწერეთ ფუნქცია, რომელიც აბრუნებს "Even" თუ რიცხვი ლუწია, "Odd" თუ კენტია. 
def even_or_odd(number):
    if number % 2 == 0:
        return "Even"
    else:
        return "Odd"